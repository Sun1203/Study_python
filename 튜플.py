# t1[0] = 2  -> 타입에러 발생 튜플은 값을 변경할 수 없음


# 인덱싱하기!
t1 = (1, 5, "이재선", "jaeseon")

print(t1[0])        # 1  -> t1의 index 0번째 값을 출력함
print(t1[1])        # 5  -> t1의 index 1번째 값을 출력함
print(t1[2])        # 이재선 -> index 2번째 값 출력
print(t1[3])        # jaeseon  -> index 3번째 값 출력


#슬라이싱하기
t1 = (1, 5, "이재선", "jaeseon", "encore", "서울", "코로나", 14)

print(t1[:2])           # (1, 5)  -> t1의 index 0번째부터 2번째직전까지 즉 1번째 값까지만 출력
print(t1[:5])           # (1, 5, '이재선', 'jaeseon', 'encore')  -> t1의 index 0번째부터 5번째 직전까지 즉 4번째 값까지 출력
print(t1[-1])           # 14  -> t1의 index 뒤에서 첫번째 값 출력


