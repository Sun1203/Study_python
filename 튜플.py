'''
tuple(튜플)
    
    1. t2 = (1,)처럼 단지 1개의 요소만을 가질 때는 요소 뒤에 콤마(,)를 반드시 붙여야함
    
    2. t4 = 1, 2, 3, 4 처럼 괄호()를 생략해도 무방함!

튜플(tuple)과 리스트(list)의 차이점
    
    1. 리스트의 항목 값은 변화가 가능하고 튜플의 항목 값은 변화가 불가능 함

'''    



# del : 튜플 요소값을 삭제하려 할 때
